                        <h2>Physical Learning</h2>
                        <p>Physical students are kinaesthetic, or "hands on" learners.  They retain information by doing or performing tasks. They learn from physcal activities and manipulating physical materials.  Some attributes of this style are as follows: Do you remember what you have done more than what was said or seen? Do you doodle while listening? Are you good at drawing? Do you figit, tap your fingers or feet when you sit. Do you enjoy tasks that involve using your hands? Are you athletic? Would you rather learn a task by doing it? These characteristics are often found in physical learners.</p>
                    </div>
                    <div class="span4 tipsubcta">
                        <h2>Visual Learning</h2>
                        <p>Visual learners retain more information by seeing the information as images pictures or colors. Some attributes of visual learners are as follows: Are you good at spelling but forget names? Do you remember pictures, charts or diagrams easily? Are you a reader/observer? Do you think in pictures? Do you enjoy seeing things and enjoy visual stimulation? Do you pay attention to body language and facial expressions? Do you have to see things in your mind to remember them? These characteristics are often found in visual learners.</p>
