                        <h2>Auditory Learning</h2>
                        <p>Auditory learners retain more information by listening.  Some attributes of auditory learners may be as follows. Do you understanding spoken directions easier than written directions?  Do you remember what people say rather than what you read? Are you good at explaining things? Do you remember names? Can you remember accurate details of information from conversations or lectures? Do you have a musical talent? Do you sing hum or whistle to yourself?  Can you remember jokes?  These characteristics are often found in auditory learners.</p>
                    </div>
