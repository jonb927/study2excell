                <h2>Tips for Physical Learners</h2>
                <ul>
                    <li>Write when your reading or talking</li>
                    <li>Write everything more than once.  Notes -> Lists -> Flashcards -> Summary's</li>
                    <li>Spend extra time in any labs</li>
                    <li>Walk around or stand while studying</li>
                    <li>Draw charts diagrams or pitures to relate information</li>
                    <li>Use props to represent information</li>
                </ul>
                <h2>Tips for Visual Learners</h2>
                <ul>
                    <li>Watch videos checkout the links above</li>
                    <li>Color Code and highlight notes</li>
                    <li>Create graphs and charts</li>
                    <li>Make a Picture Journal as a review</li>
                    <li>Study in quiet places away from verbal distractions</li>
                    <li>Compare study notes</li>
