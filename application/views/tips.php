                        <h3>Noteworthy Tips</h3>
                        <p>Flashcards are old and antiquated, but they still work.  Whatever type of learner you are flashcards can fufill all the necessary stimuli that each type of learner needs.  A visual learner can see the information on the cards and keeps the picture as their memory. The auditory learner can sound out the cards as well as the answers. The physical learner has something to hold on to and shuffle around also the writing of the cards is helpful for him or her. So don't forget this old but essential tool in your study toolbox.</p>
                    </div>
                    <div class="span4 tipsubcta" >
                        <h3>Lets Play Memory</h3>
                        <p>Physical learners need something to feel, touch or do to remember information. Playing a memory game can help to  improve your memory recall. Memory Timeline is a great game to play to help memory recall in subjects where the order of information is important. Organize cards or objects to represent information and place the information in a row on the table or a floor. Label each item appropriately in the corect order. To play just remove the items shuffle them around and see if you can put them back in the right place. </p>
                    </div>
                    <div class="span4 tipsubcta" >
                        <h3>Staying Focused</h3>
                        <p>Three bullets on keeping focused. 1. Know yourself 2. know your goal 3. Eliminate personal distractions.  these 3 keys go hand in hand.  Be true to yourself and know your strengths and weaknesses.  Keep your goal S.M.A.R.T. Specific, Measurable, Achievable, Realistic, and Timely.  Schedule your study time and break it apart.(i.e 1hr = (3) 20 min sessions with breakes between). Keep what's important in front of you. Turn off TV's, Smart Phones, Video Games, Music and whatever else you know that distracts you while studying. Make those minutes count.</p>
